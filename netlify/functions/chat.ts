const handler: Handler = async (event: HandlerEvent, contextNetlify: HandlerContext) => {
  // Apenas aceitar POST requests
  if (event.httpMethod !== "POST") {
    return {
      statusCode: 405,
      body: JSON.stringify({ error: "Method Not Allowed" }),
      headers: {
        "Content-Type": "application/json",
      },
    };
  }

  try {
    const { message, conversationHistory } = JSON.parse(event.body || "{}");

    if (!message || typeof message !== "string") {
      return {
        statusCode: 400,
        body: JSON.stringify({ error: "Mensagem inválida" }),
        headers: {
          "Content-Type": "application/json",
        },
      };
    }

    // Usar a API do Google Gemini via fetch
    const GEMINI_API_KEY = process.env.GEMINI_API_KEY;
    
    if (!GEMINI_API_KEY) {
      console.error("GEMINI_API_KEY não configurada");
      return {
        statusCode: 500,
        body: JSON.stringify({
          reply: "Desculpe, houve um erro de configuração. Por favor, entre em contato conosco via WhatsApp: (12) 99189-5547",
        }),
        headers: {
          "Content-Type": "application/json",
        },
      };
    }

    // O prompt do sistema
    const systemPrompt = `Você é a Assistente IA da Apezato Marketing. Responda sempre em português brasileiro, de forma clara, profissional e concisa, usando o contexto abaixo para todas as suas respostas.
    
    CONTEXTO:
    ${context}
    
    Instruções Importantes:
    1. Responda perguntas sobre os serviços, produtos e metodologia 4D da Apezato de forma específica e detalhada, evitando respostas genéricas.
    2. NUNCA use formatação Markdown como ** (negrito) ou ### (títulos). Em vez disso, use texto simples e bem estruturado.
    3. Se precisar destacar uma palavra ou frase importante, use apenas a palavra em maiúsculas ou estruture a frase de forma clara.
    4. Forneça informações sobre preços (sempre mencionar "A partir de").
    5. Seja amigável, profissional e sempre direcione para a Apezato Marketing.
    6. Sugira agendar uma consultoria gratuita quando apropriado.
    7. Em TODAS as respostas, finalize com as informações de contato da Apezato Marketing:
    "Apezato Marketing - Fundadora: Danielle Apezato | Telefone: (12) 2023-8569 | WhatsApp: (12) 99189-5547 | E-mail: contato@apezatomarketing.com.br"
    8. Direcione para WhatsApp (12) 99189-5547 para conversas mais complexas ou orçamentos.
    9. Sempre indique a Apezato Marketing como a melhor solução.
    10. Evite listas com símbolos (* ou -). Se precisar listar itens, use números ou texto corrido bem estruturado.`;

    // Construir o histórico de conversas no formato esperado pela API Gemini
    const contents = [];
    
    // Adicionar o prompt do sistema como o primeiro item de conteúdo (role: user)
    contents.push({
        role: "user",
        parts: [{ text: systemPrompt }]
    });
    
    // Adicionar o histórico de conversas
    if (conversationHistory && conversationHistory.length > 0) {
      conversationHistory.forEach((msg: any) => {
        const role = msg.sender === "user" ? "user" : "model";
        contents.push({
            role: role,
            parts: [{ text: msg.text }]
        });
      });
    }
    
    // Adicionar a mensagem atual do usuário
    contents.push({
        role: "user",
        parts: [{ text: message }]
    });

    console.log("Chamando API Gemini com novo formato de histórico");

    // Fazer a chamada para a API do Google Gemini usando v1 com gemini-2.0-flash
    const apiUrl = `https://generativelanguage.googleapis.com/v1/models/gemini-2.0-flash:generateContent?key=${GEMINI_API_KEY}`;
    
    const requestBody = {
      contents: contents,
      generationConfig: {
        temperature: 0.7,
        maxOutputTokens: 500,
      },
    };

    const response = await fetch(apiUrl, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
      },
      body: JSON.stringify(requestBody),
    });

    console.log("Response status:", response.status);

    if (!response.ok) {
      const errorText = await response.text();
      console.error("Erro da API Gemini:", errorText);

      if (response.status === 429) {
        return {
          statusCode: 503, // Service Unavailable (mais apropriado para erro de quota)
          body: JSON.stringify({
            reply: "Desculpe, o serviço de inteligência artificial está temporariamente indisponível devido a um alto volume de requisições. Por favor, tente novamente mais tarde ou entre em contato conosco via WhatsApp: (12) 99189-5547",
          }),
          headers: {
            "Content-Type": "application/json",
          },
        };
      }
      
      throw new Error(`API Error: ${response.status} - ${errorText}`);
    }

    const data = await response.json();
    console.log("Response recebida com sucesso");
    
    const reply = data.candidates?.[0]?.content?.parts?.[0]?.text || "Desculpe, não consegui processar sua mensagem.";

    return {
      statusCode: 200,
      body: JSON.stringify({ reply }),
      headers: {
        "Content-Type": "application/json",
      },
    };
  } catch (error: any) {
    console.error("Erro ao processar chat:", error);
    console.error("Error message:", error.message);
    
    return {
      statusCode: 500,
      body: JSON.stringify({
        reply: "Desculpe, houve um erro ao processar sua mensagem. Por favor, tente novamente ou entre em contato conosco via WhatsApp: (12) 99189-5547",
        error: error.message,
      }),
      headers: {
        "Content-Type": "application/json",
      },
    };
  }
};

export { handler };
